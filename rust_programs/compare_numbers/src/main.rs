fn main() {
    let num1 = 40; // First number
    let num2 = 30; // Second number

    if num1 > num2 {
        println!("{} is greater than {}", num1, num2);
    } else if num1 < num2 {
        println!("{} is less than {}", num1, num2);
    } else {
        println!("{} is equal to {}", num1, num2);
    }
}
